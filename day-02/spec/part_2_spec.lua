local m = require "part2"
pcall(function() require("lldebugger").start() end)
describe("solutions", function()
    it("find invalid number", function()
        assert.are.equal(true, m.is_invalid_number(11))
        assert.are.equal(true, m.is_invalid_number(565656))
    end)
    it("finds invalid numbers in range", function()
        assert.are.same({ 999, 1010 }, m.find_invalid_in_range(m.parse_range("998-1012")))
        assert.are.same({ 565656 }, m.find_invalid_in_range(m.parse_range("565653-565659")))
        assert.are.same({ 824824824 }, m.find_invalid_in_range(m.parse_range("824824821-824824827")))
        assert.are.same({ 2121212121 }, m.find_invalid_in_range(m.parse_range("2121212118-2121212124")))
    end)
    it("test input", function()
        assert.are.equal(4174379265, m.solution("./inputs/test.txt"))
    end)
    it("input", function()
        assert.are.equal(36037497037, m.solution("./inputs/input.txt"))
    end)
end)

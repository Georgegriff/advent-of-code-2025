local m = require "part2"
pcall(function() require("lldebugger").start() end)
describe("solutions", function()
    it("test input", function()
        assert.are.equal(3121910778619, m.solution("./inputs/test.txt"))
    end)
    it("finds largest 12 digit number in sequence", function()
        assert.are.equal(987654321111, m.largest_number("987654321111111"))
        assert.are.equal(811111111119, m.largest_number("811111111111119"))
        assert.are.equal(434234234278, m.largest_number("234234234234278"))
        assert.are.equal(888911112111, m.largest_number("818181911112111"))
    end)
    it("joins number array", function()
        assert.are.equal("4567", m.number_array_to_str({ 4, 5, 6, 7 }))
    end)
    it("input", function()
        assert.are.equal(170025781683941, m.solution("./inputs/input.txt"))
    end)
end)
